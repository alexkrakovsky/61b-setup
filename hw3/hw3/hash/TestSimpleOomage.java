    @Test
    public void testHashCodeDeterministic() {
        SimpleOomage so = SimpleOomage.randomSimpleOomage();
        int hashCode = so.hashCode();
        for (int i = 0; i < 100; i += 1) {
            assertEquals(hashCode, so.hashCode());
        }
    }

    @Test
    public void testHashCodePerfect() {
        List<SimpleOomage> OomageList = new ArrayList<>();
        for (int r = 0; r <= 50; r += 5) {
            for (int g = 0; g <= 50; g += 5) {
                for (int b = 0; b <= 50; b += 5) {
                    OomageList.add(new SimpleOomage(r, g, b));
                }
            }
        }
        for (SimpleOomage s : OomageList) {
            for (SimpleOomage t : OomageList) {
                int sHash = s.hashCode();
                int tHash = t.hashCode();
                if (s.equals(t)) {
                    assertEquals(sHash, tHash);
                }
                else {
                    assertNotEquals(sHash, tHash);
                }
            }

        }
    }




